    public void setCurrentFont(String fontName) {
        txtFont.setText(fontName);
        int pos = 0;
        for (int i = 0; i < font.size(); i++) {
            if (fontName.compareToIgnoreCase((String) font.get(i)) == 0) {
                pos = i;
            }
        }
        listFont.setSelectedIndex(pos);
    }

    public void setStyle(int style) {
        if (style == Font.BOLD) {
            txtStyle.setText("Bold");
        } else if (style == Font.PLAIN) {
            txtStyle.setText("Plain");
        } else {
            txtStyle.setText("Italic");
        }
        listStyle.setSelectedValue(txtStyle.getText(), true);
    }

    public void setFont() {
        GraphicsEnvironment localGraphicsEnvironment = GraphicsEnvironment.getLocalGraphicsEnvironment();
        String[] fontName = localGraphicsEnvironment.getAvailableFontFamilyNames();
        for (int i = 0; i < fontName.length; i++) {
            font.add(fontName[i]);
        }
        listFont.setListData(font);
    }

    public void setSize() {
        for (int i = 10; i <= 50; i += 5) {
            size.add(i);
        }
        listSize.setListData(size);
    }

