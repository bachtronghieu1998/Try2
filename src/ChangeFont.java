    public void setFontSample() {
        String font = txtFont.getText();
        String temp = txtStyle.getText();
        String size = txtSize.getText();
        int style = 0;
        if (temp.compareToIgnoreCase("plain") == 0) {
            style = Font.PLAIN;
        } else if (temp.compareToIgnoreCase("bold") == 0) {
            style = Font.BOLD;
        } else {
            style = Font.ITALIC;
        }
        int tmpSize = (int) Float.parseFloat(size);
        txtSample.setFont(new Font(font, style, tmpSize));
    }
